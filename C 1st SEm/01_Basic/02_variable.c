#include<stdio.h>
int main()
{
    int a=5; // integer 
    float b=8.5; // real number 
    char c='u';  //character
    int d=45+9;    // we can also perform calculations in variables
    float e=a+b;
    printf("The value of a is: %d\n",a); // %d for integer
    printf("The value of b is: %f\n",b); // %f for float
    printf("The value of c is: %c\n",c);   // %c for character
    printf("The value of d is: %d\n",d);
    printf("The sum of a and b is: %f",e);

    return 0;
}